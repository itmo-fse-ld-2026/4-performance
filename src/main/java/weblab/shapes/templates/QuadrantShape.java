package weblab.shapes.templates;

import weblab.models.Point;

/**
 * Декоратор фигуры, обеспечивающий трансформацию координат в соответствии с номером квадранта.
 *
 * <p>Этот класс позволяет использовать фигуру, определённую для первого квадранта
 * (все координаты положительны), в любом из четырёх квадрантов путём преобразования
 * координат проверяемой точки.
 *
 * <p>Преобразования по квадрантам:
 * <ul>
 *   <li>Квадрант 1: (x, y) → (x, y) — без изменений</li>
 *   <li>Квадрант 2: (x, y) → (-x, y) — отражение по оси Y</li>
 *   <li>Квадрант 3: (x, y) → (-x, -y) — отражение по обеим осям</li>
 *   <li>Квадрант 4: (x, y) → (x, -y) — отражение по оси X</li>
 * </ul>
 *
 * <p>После преобразования точка проверяется исходной фигурой, которая определена
 * для работы с неотрицательными координатами.
 *
 * @author Vladislav Dyadev
 * @version 1.0
 * @see Shape
 * @see weblab.shapes.QuadrantShapeTemplate
 */
public class QuadrantShape implements Shape {
    /**
     * Исходная фигура, определённая для первого квадранта.
     */
    private final Shape shape;

    /**
     * Номер квадранта (1, 2, 3 или 4), определяющий трансформацию координат.
     */
    private final int quadrant;

    /**
     * Конструктор декоратора фигуры с указанием квадранта.
     *
     * @param shape    исходная фигура, определённая для первого квадранта
     * @param quadrant номер квадранта (1, 2, 3 или 4)
     */
    public QuadrantShape(Shape shape, int quadrant) {
        this.shape = shape;
        this.quadrant = quadrant;
    }

    /**
     * Проверяет, содержит ли фигура точку с учётом трансформации по квадранту.
     *
     * <p>Координаты точки преобразуются в соответствии с номером квадранта,
     * после чего проверка делегируется исходной фигуре.
     *
     * @param p проверяемая точка
     * @return {@code true}, если преобразованная точка содержится в фигуре,
     *         {@code false} в противном случае
     */
    @Override
    public boolean contains(Point p) {
        double x = p.x();
        double y = p.y();
        switch (quadrant) {
            case 2 -> x = -x;
            case 3 -> { x = -x; y = -y; }
            case 4 -> y = -y;
        }
        return shape.contains(new Point(x, y, p.r()));
    }
}